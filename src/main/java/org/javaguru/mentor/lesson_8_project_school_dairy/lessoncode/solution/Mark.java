package org.javaguru.mentor.lesson_8_project_school_dairy.lessoncode.solution;

class Mark {

    private String subject;
    private int mark;

    public Mark(String subject, int mark) {
        this.subject = subject;
        this.mark = mark;
    }

    public String getSubject() {
        return subject;
    }

    public int getMark() {
        return mark;
    }
}
